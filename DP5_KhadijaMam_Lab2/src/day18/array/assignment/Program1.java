/*
 * 1.	WAP to add elements to single dimensional int array
 *  and print elements from 1D array
 */
public class Program1 {

	public static void oneDArrayDemo()
	{
		int a[]=new int[5];
		a[0]=10;
		a[1]=20;
		a[2]=24;
		a[3]=13;
		a[4]=18;
		System.out.println("Element at index 0:\t"+a[0]);
		System.out.println("Element at index 1:\t"+a[1]);
		System.out.println("Element at index 2:\t"+a[2]);
		System.out.println("Element at index 3:\t"+a[3]);
		System.out.println("Element at index 4:\t"+a[4]);

	}
	
	
	public static void main(String[] args) {
		// TODO Auto-generated method stub
		oneDArrayDemo();
	}

}
