package com.tq.fundamental;

public class Distance {

	public static void main(String[] args) {
		
		int km=5, m, cm;
		m=km*1000;
		cm=km*100000;
		System.out.println("The distance of "+km+"km in meter is "+m+"m");
		System.out.println("The distance of "+km+"km in centimeter is "+cm+"cm");
	}
}
